export function decodeHTMLSpecialChar(encoded) {
    const splited = encoded.split(/[&;]/);

    let decoded = "";
    for (let i = 0; i < splited.length; i++) {
        const str = splited[i];
        if (htmlEncodeCodes.includes(str)) {
            const codeIndex = htmlEncodeCodes.indexOf(str);
            splited.splice(i, 1, htmlDecodeCodes[codeIndex]);
        }
        decoded = splited.join("");
    }
    return decoded;
}
